
    public class Uni2Exe03 {
        public static void main(String[] args) {
            // Definindo o valor do raio
            double raio = 5.0; // Por exemplo, raio = 5
            
            // Definindo o valor de pi
            double pi = 3.14159;
            
            // Calculando a área da circunferência
            double area = pi * raio * raio;
            
            // Exibindo o resultado
            System.out.println("A área da circunferência com raio " + raio + " é: " + area);
        }
    }
    
